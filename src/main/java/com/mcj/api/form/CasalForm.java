package com.mcj.api.form;

public class CasalForm {
	private Long homem;
	private Long mulher;
	private String dataCasamento;
	private Long casalPadrinho;
	private String logradouro;
	private Long numero;
	private Long bairro;

	public Long getHomem() {
		return this.homem;
	}

	public Long getMulher() {
		return this.mulher;
	}

	public String getDataCasamento() {
		return this.dataCasamento;
	}

	public Long getCasalPadrinho() {
		return this.casalPadrinho;
	}

	public String getLogradouro() {
		return this.logradouro;
	}

	public Long getNumero() {
		return this.numero;
	}

	public Long getBairro() {
		return this.bairro;
	}
}